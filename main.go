package main

import (
	"bufio"
	"context"
	"fmt"
	"os"
	"os/signal"
	"runtime"
	"strconv"
	"strings"
	"sync"
	"sync/atomic"
	"syscall"
	"time"

	"go.mongodb.org/mongo-driver/mongo"
	"go.mongodb.org/mongo-driver/mongo/options"
)

var (
	// Amount of times the scanner connect (if OnlyConnect is true) or saved to the database successfully
	successCount = int64(0)

	// For use when going to log an error when shutting down
	shuttingDown = false
)

func main() {
	hagelslag, err := NewHagelslag()
	if err != nil {
		fmt.Printf("Error initializing hagelslag: %s\n", err)
		os.Exit(1)
	}

	err = testDBConnection(hagelslag.URI)
	if err != nil {
		fmt.Printf("Error connecting to database: %s\n", err)
		os.Exit(1)
	}

	format := "\r\033[KRate: %d | Success: %d | At: %s"

	writer := bufio.NewWriter(os.Stderr)
	defer writer.Flush()

	// Channel that will print status every second
	status := time.NewTicker(1 * time.Second).C

	signals := make(chan os.Signal, 1)
	signal.Notify(signals, syscall.SIGINT, syscall.SIGTERM)

	semaphore := make(chan struct{}, hagelslag.Rate)
	addresses := make(chan string)

	var wg sync.WaitGroup
	for range runtime.NumCPU() {
		wg.Add(1)
		go hagelslag.worker(addresses, semaphore, &wg)
	}

	ip, err := parseIP(hagelslag.StartingIP)
	if err != nil {
		fmt.Printf("Error parsing starting IP: %s\n", err)
		os.Exit(1)
	}

	portInt, err := strconv.Atoi(hagelslag.Scanner.Port())
	if err != nil {
		fmt.Printf("Error parsing port: %s\n", err)
		os.Exit(1)
	}

	port := uint16(portInt)

	// Main loop
	for {
		select {
		case <-status:
			success := atomic.LoadInt64(&successCount)
			fmt.Fprintf(writer, format, hagelslag.Rate, success, ipFromUint32(ip, port))
			writer.Flush()

		case <-signals:
			fmt.Printf("\nShutting down...\n")
			shuttingDown = true
			close(addresses)
			wg.Wait()
			address := ipFromUint32(ip, port)
			if strings.HasPrefix(address, "255.0.0.0") {
				fmt.Println("Done.")
			} else {
				fmt.Printf("Last IP: %s\n", address)
			}

			return

		default:
			if ip >= 0xFF000000 {
				signals <- syscall.SIGTERM
				continue
			}

			if isReserved(&ip) {
				os.Stderr.WriteString("\nReserved range reached, skipping to next available range.\n")
			}

			address := ipFromUint32(ip, port)

			// Get a slot to work on a task
			semaphore <- struct{}{}

			// Send the address to workers
			addresses <- address

			ip++
		}
	}
}

// For checking earlier if the database is reachable
func testDBConnection(uri string) error {
	client, err := mongo.Connect(context.TODO(), options.Client().SetServerSelectionTimeout(3*time.Second).ApplyURI(uri))
	if err != nil {
		return err
	}

	err = client.Ping(context.TODO(), nil)
	if err != nil {
		return err
	}

	err = client.Disconnect(context.TODO())
	if err != nil {
		return err
	}

	return nil
}

